package main

import "fmt"

func main() {

	// ARITIMETICOS
	soma := 1 + 2
	subtracao := 1 - 2
	divisão := 1 / 2
	multiplicacao := 1 * 2
	resto := 1 % 2

	fmt.Println(soma, subtracao, divisão, multiplicacao, resto)

	// ATRIBUICAO
	var var1 string = "Texto"
	var2 := "Texto"
	fmt.Println(var1, var2)

	// RELACIONAIS
	// > - Maior que
	// < - Menor que
	// == - Igual
	// != - Diferente
	// >= - Maior ou igual
	// <= - Menor ou igual

	//LOGICOS
	// AND &&
	// OR ||
	// NOT !
	//

	//UNARIOS
	numero := 10
	numero++     // INCREMENTA MAIS 1
	numero += 15 // INCREMENTA MAIS 15
	numero--     // DECREMENTA MAIS 1
	numero -= 15 // DECREMENTA MAIS 15
	numero *= 3  // numero * 3
	fmt.Println(numero)

	//OPERADOR TERNARIO
	// IFS
}
